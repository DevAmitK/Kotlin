package Kotlin_Basics_Syntax

/**
 * The main function is the entry
 * point of any Kotlin program. Here's how it works:
 */

fun main() {
    println("Hello, Kotlin!")
}


/**
 * fun: Indicates the declaration of a function.
 *
 * main: The name of the function. In Kotlin, this
 *    name is reserved as the entry point for program execution.
 *
 * Parentheses (): Used to define function parameters. For the
 *   main function, parameters are optional unless you need to handle
 *   command-line arguments.
 *
 * Braces {}: Denotes the body of the function where you
 *       write the logic to be executed.
 */